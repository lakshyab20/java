class Rectangle{
public static void main(String args[])
{
double l=2.5;
double b=5.0;
double area = l*b;
System.out.println("Area of Rectangle is: "+area);  //expression for calculating area of rectangle
double perimeter=2*(l+b);
System.out.println("Perimeter of Rectangle is: "+perimeter);  //expression for calculating perimeter of rectangle
}
}